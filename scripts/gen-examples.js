// Regenerates the bundled example sketches and src/renderer/examples/index.ts
// from the official arduino/arduino-examples repository.
//
// Usage:
//   node scripts/gen-examples.js [path-to-arduino-examples-checkout]
//
// If no path is given, the repo is shallow-cloned into a temp directory. Every
// .ino is copied verbatim (comments intact) into src/renderer/examples/sketches/
// under its category folder, and index.ts is rebuilt with a one-line description
// pulled from each sketch's header comment.

const fs = require('fs');
const path = require('path');
const os = require('os');
const { execSync } = require('child_process');

const REPO = 'https://github.com/arduino/arduino-examples.git';
const EXAMPLES_DIR = path.join(__dirname, '..', 'src', 'renderer', 'examples');
const SKETCHES_DIR = path.join(EXAMPLES_DIR, 'sketches');
const INDEX_FILE = path.join(EXAMPLES_DIR, 'index.ts');

function resolveSource() {
  if (process.argv[2]) return path.join(process.argv[2], 'examples');
  const tmp = fs.mkdtempSync(path.join(os.tmpdir(), 'arduino-examples-'));
  console.log('Cloning', REPO, '…');
  execSync(`git clone --depth 1 ${REPO} "${tmp}"`, { stdio: 'inherit' });
  return path.join(tmp, 'examples');
}

// Recursively collect .ino files. The first directory under examples/ is the category.
function collect(dir, category, out) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) collect(p, category || entry.name, out);
    else if (entry.name.endsWith('.ino')) out.push({ category, name: entry.name.replace(/\.ino$/, ''), absPath: p });
  }
}

// Pull a one-line description from the sketch's leading /* ... */ header comment.
function describe(code, fallback) {
  const m = code.match(/\/\*([\s\S]*?)\*\//);
  if (!m) return fallback;
  const lines = m[1].split('\n').map(l => l.replace(/^\s*\*?\s?/, '').trim()).filter(Boolean);
  const desc = lines.find((l, i) => i > 0 && !/^https?:\/\//.test(l));
  let text = (desc || lines[0] || fallback).replace(/\s+/g, ' ').trim();
  return text.length > 100 ? text.slice(0, 97) + '…' : text;
}

const prettyCategory = d => d.replace(/^(\d+)\./, '$1. ').replace(/_/g, ' ');

function main() {
  const src = resolveSource();
  fs.rmSync(SKETCHES_DIR, { recursive: true, force: true });

  const items = [];
  collect(src, '', items);
  items.sort((a, b) => (a.category + '/' + a.name).localeCompare(b.category + '/' + b.name));

  const byCat = new Map();
  const imports = [];
  items.forEach((it, i) => {
    const code = fs.readFileSync(it.absPath, 'utf8');
    const destRel = `sketches/${it.category}/${it.name}.ino`;
    const destAbs = path.join(EXAMPLES_DIR, destRel);
    fs.mkdirSync(path.dirname(destAbs), { recursive: true });
    fs.copyFileSync(it.absPath, destAbs);

    const varName = `ex${i}`;
    imports.push(`import ${varName} from './${destRel}';`);
    if (!byCat.has(it.category)) byCat.set(it.category, []);
    byCat.get(it.category).push({ name: it.name, description: describe(code, it.name), varName });
  });

  const catBlocks = [];
  for (const [cat, exs] of byCat) {
    const exLines = exs.map(e =>
      `      { name: ${JSON.stringify(e.name)}, description: ${JSON.stringify(e.description)}, code: ${e.varName} },`
    ).join('\n');
    catBlocks.push(`  {\n    name: ${JSON.stringify(prettyCategory(cat))},\n    examples: [\n${exLines}\n    ],\n  },`);
  }

  const out = `// Bundled Arduino IDE example sketches. Each .ino is imported verbatim (comments
// intact) via esbuild's text loader and offered in the Examples picker, where it
// is loaded into the editor and converted to blocks by the importer.
//
// Generated by scripts/gen-examples.js from the official arduino/arduino-examples
// repository. Do not edit by hand — re-run the script to update.

${imports.join('\n')}

export interface Example {
  name: string;
  description: string;
  code: string;
}

export interface ExampleCategory {
  name: string;
  examples: Example[];
}

export const exampleCategories: ExampleCategory[] = [
${catBlocks.join('\n')}
];
`;

  fs.writeFileSync(INDEX_FILE, out);
  console.log(`Copied ${items.length} sketches across ${byCat.size} categories.`);
}

main();
